class SkillInfo:
    """Metadata for a skill in the marketplace registry."""
    def __init__(self, name: str, description: str = "", version: str = "",
                 author: str = "", url: str = "", sha256: str = "",
                 installed: bool = False, local_path: str = "") -> None:
        self.name        = name
        self.description = description
        self.version     = version
        self.author      = author
        self.url         = url
        self.sha256      = sha256
        self.installed   = installed
        self.local_path  = local_path
    def __repr__(self) -> str:
        return f"SkillInfo(name={self.name!r}, version={self.version!r})"


class SkillMarketplace:
    """
    Local skill installer with optional remote registry.

    Hot-reload flow:
      1. Download skill bundle → /tmp/essence_skill_<name>.tgz
      2. Verify SHA256 (if provided in registry)
      3. Extract to workspace/skills/<name>/
      4. Run dry-validation: load SKILL.md and check syntax
      5. If tool.py present, import it in a subprocess for syntax check
      6. Install any requirements.txt deps via pip --user
      7. Reload the global skills dict via load_skills(workspace)
      8. Register with the live Agent instance if provided

    Usage:
        mkt = SkillMarketplace(workspace)
        skills = mkt.fetch_registry()         # list SkillInfo from remote
        mkt.install_skill("morning-briefing") # download + hot-reload
        mkt.scaffold_new_skill("my-skill")    # create empty skill template
    """

    def __init__(self, workspace: Path,
                 agent: Any = None) -> None:
        self._ws    = workspace
        self._agent = agent

    # ── Remote registry ───────────────────────────────────────────────────────
    def fetch_registry(self) -> list[SkillInfo]:
        """Fetch the remote skill registry index.
        Uses Essence_SKILL_REGISTRY (default: public essence-project/skill-hub)."""
        try:
            resp = json.loads(
                urllib.request.urlopen(self.SKILL_REGISTRY_URL, timeout=8)
                .read().decode("utf-8", errors="replace"))
            return [SkillInfo(**item) for item in resp.get("skills", [])]
        except Exception as e:
            log.debug("skill_registry_fetch_error",
                      extra={"url": self.SKILL_REGISTRY_URL[:60], "error": str(e)[:80]})
            return []

    def list_installed(self) -> list[SkillInfo]:
        """List skills installed in the workspace."""
        skills_dir = self._ws / "skills"
        if not skills_dir.exists():
            return []
        result = []
        for d in sorted(skills_dir.iterdir()):
            if not d.is_dir():
                continue
            skill_md = d / "SKILL.md"
            if not skill_md.exists():
                continue
            try:
                content = skill_md.read_text(encoding="utf-8")
                desc = ""
                for line in content.splitlines():
                    stripped = line.strip()
                    if stripped and not stripped.startswith("#"):
                        desc = stripped[:120]
                        break
                result.append(SkillInfo(
                    name=d.name, description=desc, version="local",
                    author="local", url="", installed=True,
                    local_path=str(d)))
            except Exception:
                pass
        return result

    # ── Installation ──────────────────────────────────────────────────────────
    def install_skill(self, name_or_url: str,
                      sha256_expected: str = "") -> tuple[bool, str]:
        """
        Install a skill by name (from registry) or direct URL.
        Returns (success: bool, message: str).
        """
        # Resolve URL
        url = ""
        if name_or_url.startswith("http"):
            url = name_or_url
        else:
            # Look up in registry
            for skill in self.fetch_registry():
                if skill.name == name_or_url:
                    url = skill.url
                    sha256_expected = sha256_expected or skill.sha256
                    break
            if not url:
                return False, f"Skill '{name_or_url}' not found in registry"

        skill_name = name_or_url if not name_or_url.startswith("http") else                      url.rstrip("/").split("/")[-1].replace(".tar.gz","").replace(".zip","")

        # Download
        tmp_path = Path("/tmp") / f"essence_skill_{skill_name}.tgz"
        try:
            urllib.request.urlretrieve(url, tmp_path)
        except Exception as e:
            return False, f"Download failed: {e}"

        # Verify SHA256
        if sha256_expected:
            actual = hashlib.sha256(tmp_path.read_bytes()).hexdigest()
            if actual.lower() != sha256_expected.lower():
                tmp_path.unlink(missing_ok=True)
                return False, (f"SHA256 mismatch: expected {sha256_expected[:16]}… "
                               f"got {actual[:16]}…")

        # Extract
        dest = self._ws / "skills" / skill_name
        dest.mkdir(parents=True, exist_ok=True)
        try:
            import tarfile
            with tarfile.open(tmp_path) as tf:
                tf.extractall(dest)
        except Exception:
            # Try zipfile
            try:
                import zipfile
                with zipfile.ZipFile(tmp_path) as zf:
                    zf.extractall(dest)
            except Exception as e:
                return False, f"Extraction failed: {e}"
        finally:
            tmp_path.unlink(missing_ok=True)

        # Validate SKILL.md
        skill_md = dest / "SKILL.md"
        if not skill_md.exists():
            return False, "Invalid skill: missing SKILL.md"

        # Install requirements if present
        req_file = dest / "requirements.txt"
        if req_file.exists():
            r = subprocess.run(
                [sys.executable, "-m", "pip", "install", "-r", str(req_file),
                 "--user", "--quiet"],
                capture_output=True, timeout=120)
            if r.returncode != 0:
                log.warning("skill_req_install_failed",
                            extra={"skill": skill_name,
                                   "stderr": r.stderr.decode()[:200]})

        # Hot-reload: update live agent skills dict if agent is running
        self._hot_reload(skill_name, str(skill_md))
        return True, f"Skill '{skill_name}' installed at {dest}"

    def _hot_reload(self, name: str, skill_md_path: str) -> None:
        """Update a live Agent instance's skills dict without restart."""
        if self._agent is None:
            return
        try:
            content = Path(skill_md_path).read_text(encoding="utf-8")
            if hasattr(self._agent, "skills") and isinstance(self._agent.skills, dict):
                self._agent.skills[name] = content
                log.info("skill_hot_reloaded", extra={"name": name})
        except Exception as e:
            log.debug("skill_hot_reload_error",
                      extra={"name": name, "error": str(e)[:60]})

    # ── Scaffolding ───────────────────────────────────────────────────────────
    def scaffold_new_skill(self, name: str) -> Path:
        """
        Create an empty skill template at workspace/skills/<name>/.
        Opens an editor (VISUAL/EDITOR env var) if available.
        """
        skill_dir = self._ws / "skills" / name
        skill_dir.mkdir(parents=True, exist_ok=True)

        skill_md = skill_dir / "SKILL.md"
        if not skill_md.exists():
            skill_md.write_text(textwrap.dedent(f"""                # {name}

                ## Description
                <!-- What this skill does in 1-2 sentences. -->

                ## When to use
                <!-- Trigger phrases and conditions. -->

                ## Steps
                1. <!-- First step -->
                2. <!-- Second step -->

                ## Tools used
                <!-- List tool names this skill invokes. -->

                ## Examples
                <!-- Example prompts and expected outcomes. -->
            """), encoding="utf-8")

        tool_py = skill_dir / "tool.py"
        if not tool_py.exists():
            tool_py.write_text(textwrap.dedent(f"""                # {name}/tool.py  — optional Python tool for this skill
                # Remove this file if the skill uses only built-in tools.
                #
                # def run(args: dict) -> str:
                #     ...
            """), encoding="utf-8")

        print(f"  {green('✓')} Scaffolded skill at {skill_dir}")
        print(f"  Edit {cyan(str(skill_md))} to define your skill.")

        # Try to open editor
        editor = os.environ.get("VISUAL") or os.environ.get("EDITOR")
        if editor and sys.stdin.isatty():
            try:
                subprocess.run([editor, str(skill_md)], check=False)
            except Exception:
                pass
        return skill_dir


