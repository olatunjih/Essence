def _web_ui_html(ws: 'Path') -> str:
    """
    Return the web UI HTML.  Priority order:
      1. workspace/server/index.html  — user-customised copy (scaffold output)
      2. package/static_ui.html       — Analytics Engine v3.0 full UI (bundled with package)
      3. _WEB_UI_B64 fallback         — legacy zlib+base64 blob
    """
    # Priority 1: workspace-local customised copy
    ws_html = Path(ws) / "server" / "index.html"
    if ws_html.exists():
        try:
            return ws_html.read_text(encoding="utf-8")
        except Exception:
            pass

    # Priority 2: package-bundled Analytics Engine v3.0 full UI
    _pkg_html = Path(__file__).parent / "static_ui.html"
    if _pkg_html.exists():
        try:
            return _pkg_html.read_text(encoding="utf-8")
        except Exception:
            pass

    # Priority 3: legacy zlib+base64 blob
    import zlib, base64 as _b64
    try:
        return zlib.decompress(_b64.b64decode(_WEB_UI_B64)).decode('utf-8')
    except Exception:
        return (
            '<!DOCTYPE html><html><head><title>Essence</title></head>'
            '<body><h1>Essence v29 — Analytics Engine v3.0</h1>'
            '<p>Embedded UI unavailable. Delete server/index.html and re-scaffold.</p>'
            '</body></html>'
        )


